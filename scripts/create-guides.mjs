// ========== 2. 创建学习阶段导览文章 ==========

function makeArticle(id, title, category, tags, summary, sections) {
  const sectionsStr = sections.map(s => {
    let fields = [];
    fields.push(`        {`);
    fields.push(`            title: "${s.title}",`);
    fields.push(`            body: \`${s.body}\`,`);
    if (s.mermaid) fields.push(`            mermaid: \`${s.mermaid}\`,`);
    if (s.table) {
      const headers = JSON.stringify(s.table.headers);
      const rows = JSON.stringify(s.table.rows);
      fields.push(`            table: { headers: ${headers}, rows: ${rows} },`);
    }
    if (s.tip) fields.push(`            tip: "${s.tip}",`);
    if (s.warning) fields.push(`            warning: "${s.warning}",`);
    fields.push(`        }`);
    return fields.join('\n');
  }).join(',\n');

  return `import { Article } from '../knowledge';

export const article: Article = {
    id: "${id}",
    title: "${title}",
    category: "${category}",
    tags: ${JSON.stringify(tags)},
    summary: "${summary}",
    date: "2026-04-16",
    readTime: "15 min",
    level: "入门",
    content: [
${sectionsStr}
    ]
};
`;
}

